public class Certificate {

    int id;
    float certificate;


    public Certificate(int _id,float _certificate){
        id=_id;
        certificate=_certificate;
    }
    public int getId() {
        return id;
    }


    public void setId(int id) {
        this.id = id;
    }


    public float getCertificate() {
        return certificate;
    }


    public void setCertificate(float certificate) {
        this.certificate = certificate;
    }



}
